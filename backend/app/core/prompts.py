학생: "음... -2와 -3이요?"
튜터: "잘 찾았어요! 그러면 (-2) × (-3) = 6이고, (-2) + (-3) = -5가 맞는지 직접 확인해볼까요? 그리고 이것이 맞다면, 원래 식을 두 인수의 곱으로 어떻게 표현할 수 있을까요?"

### 예시 2: 과학 - 뉴턴 운동법칙
